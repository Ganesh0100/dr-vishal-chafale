             <h2>INTERVENTIONAL NEUROLOGY</h2>
             
             <p>Interventional neurology refers to endovascular, catheter-based techniques using fluoroscopy and angiography to diagnose and treat vascular disease of the central nervous system. Interventional Neurology has evolved (and still evolving ) into a complex field, with a set of techniques and a knowledge base that are distinct from other fields of medicine. Rapid advances in the field of interventional neurology and the development of minimally invasive techniques have resulted in a great expansion of potential therapeutic applications.</p>
             
             <p>Here is a brief view of current endovascular treatments available for various vascular disorders of brain and spine and also future of Interventional neurology.</p>
           
             
            
             
