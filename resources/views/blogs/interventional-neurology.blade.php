             <h2>DIAGNOSTIC CEREBRAL CATHETER ANGIOGRAPHY</h2>
             
    <div class="list-blog  ul-li-block">
   
       <ul class="list--items">
        <i class="fa-solid fa-angle-right"></i>
        <li>Also called as Digital subtraction angiography ( DSA). Typical indications include the diagnosis of cerebral aneurysms, arteriovenous malformations, cerebral vasospasm, intracranial stenosis, arteriovenous fistula or small vessel vasculopathy including vasculitis. It is often performed just prior to a planned neurosurgical or neurointerventional procedure, as well as immediately after a neurosurgical case. Despite alternative imaging modalities and safety concerns, the actual indications for catheter angiography have not significantly decreased because of the increasing numbers of suspicious vascular findings seen on the very studies (CTA and MRA) thought to supplant it. Furthermore, since minimally invasive endovascular techniques have gained prominence and sometimes replaced open surgical techniques, catheter angiography remains an indispensable modern imaging modality. Spinal DSA is useful tool in diagnosis and treatment planning of spinal AVM, spinal Dural AV fistula.</li>
         <i class="fa-solid fa-angle-right"></i><li>3D rotational angiography allows evaluation of the opacified artery and its branches from any angle. The technique facilitates understanding of complex vascular anatomy and is a frequently used application in modern diagnostic as well as therapeutic neurovascular care especially in aneurysm and AVM treatment planning.</li>
         <i class="fa-solid fa-angle-right"></i><li>Cerebral DSA is gold standard for diagnosis of various vascular disorders like intracranial stenosis, posterior circulation stenosis, Dural AV fistulas etc. DSA is indicated when distinctions affecting treatment are unclear; for example, angiography can assist in cases in which differentiation between carotid and vertebrobasilar TIA or evolving stroke is uncertain on clinical grounds and noninvasive imaging only.</li>
         <i class="fa-solid fa-angle-right"></i><li>Although the risks associated with cerebral angiography have been gradually decreasing, the risk for any complication is approximately 1% to 5%, of which half are minor groin hematomas. Renal function should be normal as iodinated contrast dye used for DSA is nephrotoxic.</li>
    
