        <h4>ENDOVASCULAR TREATMENT OF ANEURYSMS</h4>
   
       <ul class="list--items">
      
         <i class="fa-solid fa-angle-right"></i>
         <li> Intracranial aneurysms (IAs) are localized dilations of the cerebral arteries wall and are prone to rupture, resulting in bleeding. The overall prevalence of unruptured IAs is between 2% and 3.2% in the general population with a male to female ratio of 1:2.1 It is the leading cause of hemorrhagic stroke, responsible for 85% of subarachnoid hemorrhages (SAH).</li>
         <i class="fa-solid fa-angle-right"></i>
         <li>the outcome for patients with SAH remains poor, with overall mortality rates of 25% and significant morbidity among approximately 50% of survivors.</li>
         <i class="fa-solid fa-angle-right"></i>
         <li>Long-term follow-up in the International subarachnoid aneurysm (ISAT) trial evaluating exclusively ruptured aneurysm, with a mean follow-up of nine years, has demonstrated the effectiveness of coil embolization in essentially eliminating the risk of future subarachnoid hemorrhage.</li>
         <i class="fa-solid fa-angle-right"></i>
         <li>Considerable advances have been made in the ability to use coils for endovascular treatment of intracranial aneurysms in situations that might have appeared unsuitable a few years ago. The new designs of coils, including the three-and two-dimensional configurations, have improved results. Focusing on the anatomy of the aneurysm and on the neck and dome ratio, as well as its packing with coils in a step-wise manner, has led to remarkable success in coiling of aneurysms previously considered to be difficult.</li>
        
    
       
