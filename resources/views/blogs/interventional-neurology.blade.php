      <h2>ENDOVASCULAR ASPECTS OF SECONDARY STROKE PREVENTION</h2>
             
    <div class="list-blog  ul-li-block">
   
     
        <!---->
        
        <ul class="list--items">
        <i class="fa-solid fa-angle-right"></i>
        <li> <span class="bold-blog"> Carotid Angioplasty-Stenting ( CAS )</span> Current surgical interventions to lower the risk of stroke among people with carotid artery stenosis include carotid endarterectomy (CEA) as well as carotid angioplasty and stent placement (CAS).

Carotid angioplasty with stent placement (CAS) was resurrected as an alternative treatment for revascularization of carotid artery stenosis in high-risk surgical candidates. Several Randomized control studies and comparisons with CEA have increased the popularity of CAS. These studies have focused on the safety and effectiveness of CAS. With improving devices and techniques, CAS has become safer than and as effective as surgical treatment.

Patients undergoing CAS have small embolic showers occurring frequently during the procedure. These microemboli are composed of thrombotic and plaque substances. This underlies the importance of using a distal protection device to prevent the microemboli from being released into cerebral circulation during carotid angioplasty and stenting. Distal protection devices include occlusive balloons, filter devices, and flow reversal devices.</li>

        <i class="fa-solid fa-angle-right"></i>
        <li> <span class="bold-blog"> Procedure of CAS :</span> Procedure is performed through femoral artery access. 8F guide catheter or 6 F long sheath is navigated over Guide wire and placed in the common carotid artery. Stenosis is crossed with microwire. Over microwire distal embolization protection device ( Spider ) is placed into ICA. Angioplasty is done using noncompliant Balloon at desired pressure. Inj atropine is given to counteract bradycardia due to carotid body stimulation during angioplasty.Then stent is deployed across the stenosis. This procedure is done generally under local anaesthesia.</li>


        <i class="fa-solid fa-angle-right"></i>
        <li> <span class="bold-blog"> Vertebral artery stenting :</span>Most common site of stenosis is at origin of vertebral arteries , also called as osteal stenosis. In some cases traumatic / spontaneous dissection of vertebral artery results in flow-limiting narrowing necessitating stenting. Stenting is generally required in patients with severe stenosis causing symptoms of Vertebro-Basilar insufficiency. Procedure of vertebral artery stenting is generally done under local anaesthesia and stents used are balloon mounted drug eluting stents.</li>

        <i class="fa-solid fa-angle-right"></i>
        <li> <span class="bold-blog"> Angioplasty-stenting of intracranial Stenosis :</span>Intracranial stenosis is responsible for 8–10% of all ischemic strokes. There is a high yearly rate of recurrent strokes in patients with intracranial stenosis that has been estimated at approximately 8–12% and in those not taking antithrombotic treatment, the rate of recurrent ischemic stroke events can be even higher and has been estimated at 52%.

Percutaneous transluminal angioplasty with possible stent placement has been recommended for treatment of intracranial stenosis especially for patients not responding to medical treatment. The long-term follow-up has suggested ipsilateral stroke prevention of up to 96% for the first year and approximately 87% for up to the third year after interventional treatment.

For symptomatic patients with >50% intracranial stenosis who have failed medical therapy, balloon angioplasty with or without stenting should be considered. Patients who have an asymptomatic intracranial arterial stenosis should first be counseled regarding optimizing medical therapy. There is insufficient evidence to make definite recommendations regarding endovascular therapy in asymptomatic patients with severe intracranial atherosclerosis. Contraindications for intracranial stenting are inability to have antiplatelet therapy and/or anticoagulation and highly calcified lesions or anatomy that prevents endovascular access. In India balloon mounted drug eluting stents are available and widely used for intracranial stenosis. Advantage of these stents are lower rate of restenosis.</li>

        <i class="fa-solid fa-angle-right"></i>
        <li> <span class="bold-blog">Procedure of Intracranial stenting :</span>Procedure is performed through femoral artery access. 6F guide catheter is navigated over Guide wire and placed in the internal carotid artery / vertebral artery. Stenosis is crossed with microwire. Angioplasty is done using noncompliant ballloon slowly and carefully at desired pressure. Then stent is placed across the stenosis under fluoroscopic guidance.</li>
      
        </ul>
        
        
