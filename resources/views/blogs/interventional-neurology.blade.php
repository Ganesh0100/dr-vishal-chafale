        <h4>ENDOVASCULAR TREATMENT OF CEREBRAL VENOUS THROMBOSIS ( CVT )</h4>
   
       <ul class="list--items">
      
         <i class="fa-solid fa-angle-right"></i>
         <li>    Cerebral venous thrombosis (CVT) can occur in the form of cortical venous thrombosis, venous sinus thrombosis, deep venous thrombosis, jugular venous thrombosis, or various combinations of the above. CVT has high mortality rate ranging from 5–30%. The interruption of outflow in the brain circulation leads to augmentation in the pressure of the entire system with venous hypertension, intracranial hypertension, and hemorrhagic events.</li>
         <i class="fa-solid fa-angle-right"></i>
         <li>The main goal of the treatment of CVT should be the recanalization of venous drainage system with complete reestablishment of normal brain circulation. The treatment of choice is IV anticoagulation followed by local thrombolysis where indicated. Endovascular treatment is indicated for patients unable to receive antico-agulation and for those who deteriorate despite anticoagulation heparin. Endovascular therapy is also indicated in high-risk categories including those with seizures, coma, disturbed consciousness, deep cerebral vein thrombosis, posterior fossa involvement, and/or pro-gressive focal deficits.</li>
         <i class="fa-solid fa-angle-right"></i>
         <li>The endovascular route used is transvenous through the femoral vein, navigating the catheter into the venous circulation and final placement in the matrix of thrombus. The thrombolytics are given as bolus dose followed by infusion over a period of hours for a better recanalization. The rate of recanalization (partial and total) ranges from 70–95%. Drugs used are urokinase and rtPA.</li>
        
    
       
        </ul>
  
