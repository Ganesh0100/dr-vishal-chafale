        <h4>ENDOVASCULAR TREATMENT OF ACUTE ISCHEMIC STROKE</h4>
   
       <ul class="list--items">
      
         <i class="fa-solid fa-angle-right"></i>
         <li>The corner stone of the treatment in acute ischemic stroke (AIS) is revascularization. It was only in 1995 when National Institute of Neurological Disorders and Stroke rtPA Stroke Trial (NINDS Study Group) revolutionalized the management of AIS using recombinant tissue plasminogen activator (rtPA) within 3hrs reducing the stroke morbidity by 30%. Eventually the window period was increased to 4.5 hrs after ECASS III. Inj rtPA ( Actilyse ) and Tenectaplase ( Tenectase) are the IV thrombolytic agents approved for acute Ischemic Stroke and both are available in India. Dose of Inj Actilyse is 0.9 mg/kg (10 % as bolus and remaining as IV infusion over 60 mins ). It can be given upto 4.5 hrs from symptom onset. Inj Tenectaplase is approved by DGCA ,India. It can be given upto 3 hrs from symptom onset and its dose is 0.25 mg/kg ( given as bolus dose). Unfortunately, intravenous thrombolysis (IVT) has higher failure rate in large vessel occlusion (LVO).</li>
         <i class="fa-solid fa-angle-right"></i>
         <li>Intra-arterial urokinase achieved 66% recanalization in LVO and also prolonged the window period for acute stroke intervention upto 6 hours. In this procedure , drug (urokinase or rtPA) is administered through microcatheter at the site of occlusion. This targeted delivery of drug also minimises systemic side effects of drug. But there is minimal increase of risk of hemorrhagic complications. Though FDA has not approved intraarterial rtPA , it can be used in selected cases within 6 hrs of Stroke onset. American Stroke Association (ASA) has given Class 2 recommendation for this treatment. Thus, while both these therapies improved stroke outcomes, a proportion of patients having large vessel occlusions were not amenable to these measures.</li>
         
         <i class="fa-solid fa-angle-right"></i>
         <li>Mechanical thrombectomy (MT) with newer thrombectomy devices have many advantages compared to thrombolysis in LVO stroke. A recent review of literature of five randomized control trials (RCT) proved superiority of MT using stent retrievers over best medical management using IVT. Recently published study of Mechanical Thrombectomy in Acute Ischemic Stroke due to large vessel occlusion by Singh et al showed results comparable with Published RCTs and shows feasibility of this procedure in real world scenario like India. The Merci Retrieval system was the first FDA approved treatment option for embolectomy in cerebral arteries. The Penumbra Stroke System (Penumbra, Alameda) was also approved by FDA in 2008 and is the most widely used thromboaspiration device in the US. Newer generation stent retrievers available give more success rate with fewer complications. Solitaire, Trevo, Eric etc are the stentrivers available in India. With the use of ballon guide catheters ( Merci, Cello, Flowgate) with stentrivers , recanalization rate is over 80%. Recanalization rates with thromboaspiration or stentrivers are comparable and both techniques are equally efficacious. Sometimes both techniques has to be used ( Solumbra technique).</li>
         
         <i class="fa-solid fa-angle-right"></i>
         <li>Mechanical Thrombectomy is recommended upto 6 hours of symptom onset. But according to new ASA/AHA 2018 guidelines, in selected cases it can be done in patients with 6-24 hours of symptom onset ! Patients with large vessel occlusion requiring mechanical Thrombectomy should also receive IV thrombolysis and then shifted to Cathlab for mechanical Thrombectomy ( Bridging Thrombolysis ). Procedure of mechanical Thrombectomy : Procedure is performed through femoral artery access. 8F balloon guide catheter is navigated over Guide wire and placed in the ipsilateral cervical internal carotid artery. The microcatheter is navigated distal to the clot over microwire. Solitaire stentriver is then delivered through the microcatheter and deployed over the thrombus. Solitaire serves dual function, namely, immediate flow restoration by creating temporary bypass through the thrombus and also acts as a clot retriever, trapping thrombus into its cells The balloon of guide catheter is inflated with contrast to provide proximal ICA occlusion and flow arrest during the recovery of the stent retriever. Subsequently, the Solitaire and microcatheter are slowly recovered as a unit under constant aspiration with 50-mL syringe through the balloon guide catheter.</li>
    
       
        </ul>

        
        
