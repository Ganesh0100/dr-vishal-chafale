<h2>Transient Ischemic Attack (TIA)</h2>

<p>A transient ischemic attack (TIA) — sometimes know-n as a ministroke — is a temporary period of symptoms similar to
those you'd have in a stroke. A temporary decrease in blood supply to part of your brain causes TIAs, which may last as
little as five minutes.
</p>

<p>Like an ischemic stroke, a TIA occurs when a clot or debris blocks blood flowto part of your nervous system — but there
is no permanent tissue damage and no lasting symptoms.
</p>

<p>Seek emergency care even if your symptoms seem to clear up. Having a TIA puts you at greater risk of having a full-
blown stroke, causing permanent damage later. If you've had a TIA, it means there's likely a partially blocked or
narrowed artery leading to your brain or a clot source in the heart.
</p>
