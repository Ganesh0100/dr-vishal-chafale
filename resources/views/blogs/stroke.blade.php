             <h2>Stroke</h2>
             
             <p>A stroke is a "brain attack" .A stroke occurs when the blood supply to part of your brain is interrupted or reduced,
depriving brain tissue of oxygen and nutrients. Within minutes, brain cells begin to die. When brain cells die during a
stroke, abilities controlled by that area of the brain such as memory and muscle control are lost.</p>

<p>How a person is affected by their stroke depends on where the stroke occurs in the brain and how much the brain is
damaged. For example, someone who had a small stroke may only have minor problems such as temporary weakness of
an arm or leg. people who have larger strokes may be permanently paralyzed on one side of their body or lose their
ability to speak. Some people recover completely from strokes, but more than 2/3 of survivors will have some type of
disability.
</p>
