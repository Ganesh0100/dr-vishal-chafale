    <h3><a href="#">Nerve Conduction Studies and Electromyography (NCS-EMG)</a></h3>
    <h4>What is it?</h4>
    <div class="ft-project-overview-list-item ul-li-block">
        <ul>
            <li>NCS measures the speed and strength of electrical signals in the nerves, while EMG assesses the electrical activity of muscles.</li>
        </ul>
    </div>

    <h4>Uses</h4>
    <div class="ft-project-overview-list-item ul-li-block">
        <ul>
            <li>Diagnosing nerve damage or dysfunction (neuropathy, carpal tunnel syndrome).</li>
            <li>Identifying muscle disorders (myopathy).</li>
            <li>Evaluating motor neuron diseases (ALS).</li>
        </ul>
    </div>

</div>
          <!---->
          <div class="ft-blog-feed-title-text">

    <h3><a href="#">Visual Evoked Responses (VER)</a></h3>
    <h4>What is it ?</h4>
    <div class="ft-project-overview-list-item ul-li-block">
        <ul>
            <li>VER tests measure the electrical responses of the brain to visual stimuli, such as flashing lights or patterns.</li>
        </ul>
    </div>

    <h4>Uses</h4>

    <div class="ft-project-overview-list-item ul-li-block">
        <ul>
            <li>Diagnosing optic nerve disorders.</li>
            <li>Assessing visual function in conditions like multiple sclerosis.</li>
            <li>Evaluating vision in non-verbal or young patients.</li>
        </ul>
    </div>

</div>
<!---->

<div class="ft-blog-feed-title-text">
    <h3><a href="#">Brainstem Auditory Evoked Responses (BERA)</a></h3>
    <h4>What is it?</h4>
    <div class="ft-project-overview-list-item ul-li-block">
        <ul>
            <li>BERA measures the electrical activity in the brainstem in response to auditory stimuli, like clicks or tones.</li>
        </ul>
    </div>
    
    <h4>Uses</h4>
    <div class="ft-project-overview-list-item ul-li-block">
        <ul>
            <li>Diagnosing hearing loss and auditory nerve disorders.</li>
            <li>Evaluating brainstem function in cases of head injury or neurological disease.</li>
            <li>Screening for hearing issues in newborns and infants.</li>
        </ul>
    </div>
</div>

<!---->

<div class="ft-blog-feed-title-text">

    <h3><a href="#">Somatosensory Evoked Potentials (SSEP)</a></h3>
    <h4>What is it?</h4>
    <div class="ft-project-overview-list-item ul-li-block">
        <ul>
            <li>SSEP tests measure the electrical responses of the brain to sensory stimuli, such as mild electrical pulses applied to the skin.</li>
        </ul>
    </div>

    <h4>Uses</h4>
    <div class="ft-project-overview-list-item ul-li-block">
        <ul>
            <li>Diagnosing and monitoring spinal cord disorders.</li>
            <li>Evaluating nerve pathways in cases of multiple sclerosis.</li>
            <li>Assessing sensory function during surgeries involving the spinal cord.</li>
        </ul>
    </div>

</div>

